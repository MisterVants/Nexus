import Foundation

class TimeMachine {
    
    private var date = Date()
    
    func travel(by timeInterval: TimeInterval) {
        date = date.addingTimeInterval(timeInterval)
    }
    
    func travel(by timeInterval: Int) {
        travel(by: TimeInterval(timeInterval))
    }
    
    func getDate() -> Date {
        return date
    }
}
